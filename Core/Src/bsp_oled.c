static const uint8_t *OLED_GetCnGlyph16(const char *utf8)
{
    uint8_t i;

    for (i = 0U; i < (sizeof(g_cn_glyphs) / sizeof(g_cn_glyphs[0])); i++)
    {
        if ((g_cn_glyphs[i].utf8[0] == utf8[0]) &&
            (g_cn_glyphs[i].utf8[1] == utf8[1]) &&
            (g_cn_glyphs[i].utf8[2] == utf8[2]))
        {
            return g_cn_glyphs[i].bitmap;
        }
    }

    return NULL;
}

static void OLED_DrawChar(uint8_t x, uint8_t page, char ch)
{
    uint8_t i;
    const uint8_t *glyph = OLED_GetGlyph(ch);
    uint16_t index = (uint16_t)page * OLED_WIDTH + x;

    if ((page >= OLED_PAGES) || (x > (OLED_WIDTH - 6U)))
    {
        return;
    }

    for (i = 0U; i < 5U; i++)
    {
        g_oled_buffer[index + i] = glyph[i];
    }
    g_oled_buffer[index + 5U] = 0x00U;
}

static void OLED_DrawCnChar16(uint8_t x, uint8_t page, const uint8_t *glyph)
{
    uint8_t i;
    uint16_t upper_index;
    uint16_t lower_index;

    if ((glyph == NULL) || (page >= (OLED_PAGES - 1U)) || (x > (OLED_WIDTH - 16U)))
    {
        return;
    }

    upper_index = (uint16_t)page * OLED_WIDTH + x;
    lower_index = (uint16_t)(page + 1U) * OLED_WIDTH + x;
    for (i = 0U; i < 16U; i++)
    {
        g_oled_buffer[upper_index + i] = glyph[i];
        g_oled_buffer[lower_index + i] = glyph[16U + i];
    }
}

static void OLED_Refresh(void)
{
    uint8_t page;

    for (page = 0U; page < OLED_PAGES; page++)
    {
        OLED_WriteCommand(0xB0U + page);
        OLED_WriteCommand(0x00U);
        OLED_WriteCommand(0x10U);
        OLED_WriteData(&g_oled_buffer[page * OLED_WIDTH], OLED_WIDTH);
    }
}

