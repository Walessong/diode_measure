void OLED_ClearAll(void)
{
    memset(g_oled_buffer, 0, sizeof(g_oled_buffer));
    OLED_Refresh();
}

void OLED_ShowStringLine(uint8_t line, const char *str)
{
    uint8_t x = 0U;
    uint16_t base;

    if (line >= OLED_PAGES)
    {
        return;
    }

    base = (uint16_t)line * OLED_WIDTH;
    memset(&g_oled_buffer[base], 0, OLED_WIDTH);
    while ((*str != '\0') && (x <= (OLED_WIDTH - 6U)))
    {
        OLED_DrawChar(x, line, *str);
        x += 6U;
        str++;
    }
    OLED_Refresh();
}

void OLED_ShowStringRow16(uint8_t row, const char *str)
{
    uint8_t page;
    uint8_t x = 0U;
    uint16_t base;
    const uint8_t *glyph;

    page = (uint8_t)(row * 2U);
    if (page >= (OLED_PAGES - 1U))
    {
        return;
    }

    base = (uint16_t)page * OLED_WIDTH;
    memset(&g_oled_buffer[base], 0, OLED_WIDTH);
    memset(&g_oled_buffer[base + OLED_WIDTH], 0, OLED_WIDTH);

    while ((*str != '\0') && (x < OLED_WIDTH))
    {
        if (((uint8_t)*str) < 0x80U)
        {
            if (x > (OLED_WIDTH - 6U))
            {
                break;
            }
            OLED_DrawChar(x, page, *str);
            x += 6U;
            str++;
        }
        else
        {
            if (x > (OLED_WIDTH - 16U))
            {
                break;
            }
            glyph = OLED_GetCnGlyph16(str);
            if (glyph != NULL)
            {
                OLED_DrawCnChar16(x, page, glyph);
            }
            x += 16U;
            str += 3;
        }
    }

    OLED_Refresh();
}

